package model;

public class Calculos {

	public float CalcAdi(float n1, float n2) {
		float Res = n1 + n2;
		return Res;
	}

	public float CalcSub(float n1, float n2) {
		float Res = n1 - n2;
		return Res;
	}

	public float CalcMult(float n1, float n2) {
		float Res = n1 * n2;
		return Res;
	}

	public float CalcDiv(float n1, float n2) {
		float Res = n1 / n2;
		return Res;
	}
}